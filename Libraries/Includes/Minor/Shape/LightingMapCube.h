	class LightingMapCube : public ShapeBase
	{
	public:
		static LightingMapCube* create(std::string tex, std::string map)
		{
			LightingMapCube* cube = new LightingMapCube(lighting_map_cube_vertices, sizeof(lighting_map_cube_vertices));
			cube->_shader = new Shader("texture.vs", "lightingMaps.fs");
			cube->init();
			cube->bindTexture(tex);
			cube->bindMap(map);

			return cube;
		}

		static LightingMapCube* create(std::string tex, std::string map, std::string fs)
		{
			LightingMapCube* cube = new LightingMapCube(lighting_map_cube_vertices, sizeof(lighting_map_cube_vertices));
			cube->_shader = new Shader("texture.vs", fs.c_str());
			cube->init();
			cube->bindTexture(tex);
			cube->bindMap(map);

			return cube;
		}

		static LightingMapCube* create(std::string tex, std::string map, std::string vs, std::string fs)
		{
			LightingMapCube* cube = new LightingMapCube(lighting_map_cube_vertices, sizeof(lighting_map_cube_vertices));
			cube->_shader = new Shader(vs.c_str(), fs.c_str());
			cube->init();
			cube->bindTexture(tex);
			cube->bindMap(map);

			return cube;
		}

		LightingMapCube(float* vertices, int verSize)
		{
			_vertices = vertices;
			_verSize = verSize;
		}

		void bindTexture(const std::string texPath)
		{
			TextureManager::Inst()->LoadTexture(texPath.c_str(), _textureId, GL_BGRA, GL_RGB);
			glGenerateMipmap(GL_TEXTURE_2D);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		}

		void bindMap(const std::string mapPath)
		{
			TextureManager::Inst()->LoadTexture(mapPath.c_str(), _mapId, GL_BGRA, GL_RGB);
			glGenerateMipmap(GL_TEXTURE_2D);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
			glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		}

		virtual void interpret()
		{
			// position attribute
			glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)0);
			glEnableVertexAttribArray(0);
			// normal attribute
			glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)(3 * sizeof(GLfloat)));
			glEnableVertexAttribArray(1);
			// texCoords attribute
			glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)(6 * sizeof(GLfloat)));
			glEnableVertexAttribArray(2);
		}

		virtual void drawShape()
		{
			_shader->use();

			changeView();
			changeModel();

			glActiveTexture(GL_TEXTURE0);
			TextureManager::Inst()->BindTexture(_textureId);

			glActiveTexture(GL_TEXTURE1);
			TextureManager::Inst()->BindTexture(_mapId);

			glBindVertexArray(_vao);
			glDrawArrays(GL_TRIANGLES, 0, 36);
			glBindVertexArray(0);
		}

		virtual void destory()
		{
			GameObject::destory();

			glDeleteVertexArrays(1, &_vao);
			glDeleteBuffers(1, &_vbo);

			TextureManager::Inst()->UnloadTexture(_textureId);
			TextureManager::Inst()->UnloadTexture(_mapId);
			delete _shader;
			delete this;
		}

	protected:
		unsigned int _textureId = 1001;
		unsigned int _mapId = 1002;
	};

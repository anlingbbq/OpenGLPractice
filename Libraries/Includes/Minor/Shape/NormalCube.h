	class NormalCube : public ShapeBase
	{
	public:
		static NormalCube* create(std::string vs, std::string fs)
		{
			NormalCube* cube = new NormalCube(lighting_map_cube_vertices, sizeof(lighting_map_cube_vertices));
			cube->_shader = new Shader(vs.c_str(), fs.c_str());
			cube->init();

			return cube;
		}

		static NormalCube* create(std::string tex, std::string vs, std::string fs)
		{
			NormalCube* cube = NormalCube::create(vs, fs);
			cube->_textureId = TextureLoader::inst->LoadTexture2D(tex.c_str());

			return cube;
		}

		NormalCube(float* vertices, int verSize)
		{
			_vertices = vertices;
			_verSize = verSize;
		}

		virtual void interpret()
		{
			// position attribute
			glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)0);
			glEnableVertexAttribArray(0);
			// normal attribute
			glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)(3 * sizeof(GLfloat)));
			glEnableVertexAttribArray(1);
			// texCoords attribute
			glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid*)(6 * sizeof(GLfloat)));
			glEnableVertexAttribArray(2);
		}

		virtual void drawShape()
		{
			_shader->use();

			changeView();
			changeModel();

			if (_textureId != -1)
			{
				glActiveTexture(GL_TEXTURE0);
				glBindTexture(GL_TEXTURE_2D, _textureId);
			}

			glBindVertexArray(_vao);
			glDrawArrays(GL_TRIANGLES, 0, 36);
			glBindVertexArray(0);
		}

		virtual void destory()
		{
			GameObject::destory();

			glDeleteVertexArrays(1, &_vao);
			glDeleteBuffers(1, &_vbo);

			glDeleteTextures(1, &_textureId);
			delete _shader;
			delete this;
		}

	protected:
		unsigned int _textureId = -1;
	};
